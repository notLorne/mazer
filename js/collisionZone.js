class collisionZone{

    id;
    type;
    sizeX;
    sizeY;
    posX;
    posY;

    constructor(newId, newType, newSizeX, newSizeY, newPosX, newPosY) {
        this.id = newId;
        this.type = newType;
        this.sizeX = newSizeX;
        this.sizeY = newSizeY;
        this.posX = newPosX;
        this.posY = newPosY;
    }
}